'''Take a number from the user. Increase it by 5 using += and then multiply it by 2 using *=.
 Print the result after each operation.'''
# variable to store the number
num = float(input("Enter a number: "))
# increase the number by 5
num += 5
# print the result after increasing by 5
print("After increasing by 5:", num)
# multiply the number by 2
num *= 2
# print the result after multiplying by 2
print("After multiplying by 2:", num)
